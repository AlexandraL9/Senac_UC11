    private void atualizarListaVendas() {
        ProdutosDAO dao = new ProdutosDAO();
        ArrayList<ProdutosDTO> produtosVendidos = dao.listarProdutosVendidos();
        
        DefaultTableModel modelo = (DefaultTableModel) tabelaVendas.getModel();
        modelo.setRowCount(0); // Limpar as linhas existentes
        
        for (ProdutosDTO produto : produtosVendidos) {
            modelo.addRow(new Object[] { produto.getNome(), produto.getValor(), produto.getStatus() });
        }
    }
    
